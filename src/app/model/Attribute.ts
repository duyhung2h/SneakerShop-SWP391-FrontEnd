export class Attribute {
    _attributeId: any;
    _attributeName: any;
    _attributeDescription: any;
    _attributeImage: any;


	constructor(attributeId?: any, attributeName?: any, attributeDescription?: any, attributeImage?: any) {
		this._attributeId = attributeId;
		this._attributeName = attributeName;
		this._attributeDescription = attributeDescription;
		this._attributeImage = attributeImage;
	}
    
  }
  